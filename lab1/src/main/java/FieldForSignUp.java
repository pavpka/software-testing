public class FieldForSignUp {
    int num;
    String firstName;
    String secondName;
    String userName;
    String pass;
    String repeatPass;
    public FieldForSignUp(int num, String firstName, String secondName, String userName, String pass, String repeatPass){
        this.num = num;
        this.firstName = firstName;
        this.secondName = secondName;
        this.userName = userName;
        this.pass = pass;
        this.repeatPass = repeatPass;
    }
}
